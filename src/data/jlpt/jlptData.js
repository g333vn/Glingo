// Helper function để lấy exams theo level
export const getExamsByLevel = (level) => {
  return jlptExams[level.toLowerCase()] || [];
};

// Helper function để lấy một exam cụ thể
export const getExamById = (level, examId) => {
  const exams = getExamsByLevel(level);
  return exams.find(exam => exam.id === examId);
};

